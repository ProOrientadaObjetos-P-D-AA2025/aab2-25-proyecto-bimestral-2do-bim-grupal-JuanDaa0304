    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        try {
            int t = cmbTipoPlan.getSelectedIndex();
            Plan p = switch (t) {
                case 0 ->
                    new PlanPostPagoMinutosMegasEconomico(
                    Integer.parseInt(txtCampo[0].getText()),
                    Double.parseDouble(txtCampo[1].getText()),
                    Double.parseDouble(txtCampo[2].getText()),
                    Double.parseDouble(txtCampo[3].getText()),
                    Double.parseDouble(txtCampo[4].getText())
                    );
                case 1 ->
                    new PlanPostPagoMinutos(
                    Integer.parseInt(txtCampo[0].getText()),
                    Double.parseDouble(txtCampo[1].getText()),
                    Integer.parseInt(txtCampo[2].getText()),
                    Double.parseDouble(txtCampo[3].getText())
                    );
                case 2 ->
                    new PlanPostPagoMegas(
                    Double.parseDouble(txtCampo[0].getText()),
                    Double.parseDouble(txtCampo[1].getText()),
                    Double.parseDouble(txtCampo[2].getText())
                    );
                default ->
                    new PlanPostPagoMinutosMegas(
                    Integer.parseInt(txtCampo[0].getText()),
                    Double.parseDouble(txtCampo[1].getText()),
                    Double.parseDouble(txtCampo[2].getText()),
                    Double.parseDouble(txtCampo[3].getText())
                    );
            };
            int id = planCtrl.crear(p);
            if (id > 0) {
                JOptionPane.showMessageDialog(this, "Plan creado ID " + id);
                // limpia campos
                for (int i = 0; i < 5; i++) {
                    txtCampo[i].setText("");
                }
            } else {
                JOptionPane.showMessageDialog(this, "Error al crear plan", "Error", JOptionPane.ERROR_MESSAGE);
            }
            btnRefrescarPlan.doClick();
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Datos inválidos: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnCrearActionPerformed

