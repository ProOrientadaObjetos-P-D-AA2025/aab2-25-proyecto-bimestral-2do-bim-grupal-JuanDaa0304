    private void recargarComboClientes() {
        DefaultComboBoxModel<modelo.Cliente> m
                = (DefaultComboBoxModel<modelo.Cliente>) cmbClientes.getModel();
        m.removeAllElements();
        for (modelo.Cliente c : clienteCtrl.listar()) {
            m.addElement(c);
        }
    }

    private void recargarFacturas(String cedula) {
        DefaultTableModel m = (DefaultTableModel) tablaFacturas.getModel();
        m.setRowCount(0);
        List<Factura> lista = factCtrl.listarPorCliente(cedula);
        for (Factura f : lista) {
            m.addRow(new Object[]{
                f.getNumero(),
                f.getFechaFormateada(),
                String.format("%.2f", f.getTotal())
            });
        }
    }

