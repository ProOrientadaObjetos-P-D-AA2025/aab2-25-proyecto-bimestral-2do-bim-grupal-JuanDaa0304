    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        Cliente sel = (Cliente) cmbClientes.getSelectedItem();
        if (sel == null) {
            JOptionPane.showMessageDialog(this, "No hay cliente seleccionado");
            return;
        }
        String cedula = sel.getCedula();

        int planId;
        try {
            planId = Integer.parseInt(txtPlanID.getText().trim());
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "ID de plan inválido");
            return;
        }

        int posicion;
        try {
            posicion = Integer.parseInt(txtPosicion.getText().trim());
            if (posicion < 1 || posicion > 2) {
                throw new NumberFormatException();
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(this, "Posición debe ser 1 o 2");
            return;
        }

        Plan plan = planCtrl.buscar(planId);
        if (plan == null) {
            JOptionPane.showMessageDialog(this,
                    "No existe ningún plan con ID = " + planId,
                    "Error al asignar plan",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        Cliente cEnBD = clienteCtrl.buscar(cedula);
        if (cEnBD == null) {
            JOptionPane.showMessageDialog(this,
                    "El cliente seleccionado ya no está en la base de datos",
                    "Error al asignar plan",
                    JOptionPane.ERROR_MESSAGE);
            return;
        }

        boolean ok = clienteCtrl.asignarPlan(cedula, planId, posicion);
        if (ok) {
            JOptionPane.showMessageDialog(this,
                    "Plan asignado correctamente al cliente " + cedula,
                    "Asignar Plan",
                    JOptionPane.INFORMATION_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this,
                    "Se produjo un error al actualizar la base de datos",
                    "Error al asignar plan",
                    JOptionPane.ERROR_MESSAGE);
        }

        recargarComboClientes();
        btnRefrescar.doClick();
    }//GEN-LAST:event_btnAsignarActionPerformed

    private void btnGenerarFacActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerarFacActionPerformed
        String ced = txtCedulaFac.getText().trim();
        if (ced.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese cédula de cliente");
            return;
        }
        Cliente c = clienteCtrl.buscar(ced);
        if (c == null) {
            JOptionPane.showMessageDialog(this, "Cliente no encontrado");
            return;
        }
        try {
            Factura f = factCtrl.generarPara(c);
            JOptionPane.showMessageDialog(this,
                    "Factura " + f.getNumero() + " generada correctamente",
                    "Generar Factura",
                    JOptionPane.INFORMATION_MESSAGE
            );
            recargarFacturas(ced);
        } catch (IllegalStateException ex) {
            JOptionPane.showMessageDialog(this,
                    ex.getMessage(),
                    "Generar Factura",
                    JOptionPane.WARNING_MESSAGE
            );
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this,
                    "Error al generar factura:\n" + ex.getMessage(),
                    "Generar Factura",
                    JOptionPane.ERROR_MESSAGE
            );
        }
        txtCedulaFac.setText("");
    }//GEN-LAST:event_btnGenerarFacActionPerformed

